		System.out.println("valor1 + valor3 " + valor1 + valor3);
		System.out.println("valor1 * valor3 " + valor1 * valor3);
		
		System.out.println("valor1 + valor4 " + valor1 + valor4);
		System.out.println("valor1 * valor4 " + valor1 * valor4);
		
		System.out.println("valor2 + valor3 " + valor2 + valor3);
